def pairsum(numbers,target):
    l, r = 0, len(numbers)-1
    count=0
    while l < r:
        s = numbers[l] + numbers[r]
        if s == target:
            count+=1
            l+=1
            r-=1
        elif s < target:
            l += 1
        else:
            r -= 1
    return count
   
